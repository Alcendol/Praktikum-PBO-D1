    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            try {
                String nama = jTextField1.getText().trim();
                if (nama.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong");
                    return;
                }

                int newId = 1;
                List<Mahasiswa> list = service.getAll();
                if (!list.isEmpty()) {
                    newId = list.get(list.size() - 1).getId() + 1;
                }

                Mahasiswa mhsBaru = new Mahasiswa(newId, nama);
                service.add(mhsBaru);
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");

                jTextField1.setText("");
                refreshData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage());
            }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String idStr = jTextField3.getText().trim();

            if (idStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID harus diisi");
                return;
            }

            int id = Integer.parseInt(idStr);

            Mahasiswa mhs = service.getById(id);
            if (mhs == null) {
                JOptionPane.showMessageDialog(this, "Data mahasiswa dengan ID " + id + " tidak ditemukan");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ID " + id + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                service.delete(id);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                jTextField3.setText("");
                refreshData();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

