    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String idStr = jTextField2.getText().trim();
            String namaBaru = jTextField4.getText().trim();

            if (idStr.isEmpty() || namaBaru.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID dan Nama harus diisi");
                return;
            }

            int id = Integer.parseInt(idStr);

            Mahasiswa mhsLama = service.getById(id);
            if (mhsLama == null) {
                JOptionPane.showMessageDialog(this, "Data mahasiswa dengan ID " + id + " tidak ditemukan");
                return;
            }

            mhsLama.setNama(namaBaru);
            service.update(mhsLama);
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui");

            jTextField2.setText("");
            jTextField4.setText("");
            refreshData();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

