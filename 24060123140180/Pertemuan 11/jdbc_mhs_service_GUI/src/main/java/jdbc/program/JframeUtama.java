    private void jList_IDValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList_IDValueChanged
        // TODO add your handling code here:
        try {
            if (!evt.getValueIsAdjusting() && jList_ID.getSelectedValue() != null) {
                int selectedId = jList_ID.getSelectedValue();
                int indeks = idModel.indexOf(selectedId);
                String selectedName = NamaModel.getElementAt(indeks);
                jTextField_ID1.setText(String.valueOf(selectedId));
                jTextField_Nama2.setText(selectedName);
                jTextField_ID2.setText(String.valueOf(selectedId));
                jTextField_Nama3.setText(selectedName);}
        } catch (Exception e) {
            // TODO: handle exception
        }
    }//GEN-LAST:event_jList_IDValueChanged
    
