    private void jButton_AllDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AllDataActionPerformed
        // TODO add your handling code here:
        List<Mahasiswa> listMhs = service.getAll();
        
        idModel.clear();
        NamaModel.clear();
        for (Mahasiswa mhs : listMhs){
            idModel.addElement(mhs.getId());
            NamaModel.addElement(mhs.getNama());
        }
        updateList();
    }//GEN-LAST:event_jButton_AllDataActionPerformed

    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditActionPerformed
        // TODO add your handling code here:
        if (jTextField_ID1.getText().isEmpty()|| jTextField_Nama2.getText().isEmpty()){
            return;
        }
        try{
            int id = Integer.parseInt(jTextField_ID1.getText());
            String nama = jTextField_Nama2.getText();
            Mahasiswa mhs = service.makeMhsObject();
            mhs.setId(id);
            mhs.setNama(nama);
            service.update(mhs);
            idModel.clear();
            NamaModel.clear();
            List<Mahasiswa> listMhs = service.getAll();
            for (Mahasiswa mahasiswa : listMhs) {
                idModel.addElement(mahasiswa.getId());
                NamaModel.addElement(mahasiswa.getNama());
            }
            updateList();
        } catch (NumberFormatException e) {
            System.out.println("Invalid ID: " + e.getMessage());
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton_EditActionPerformed

    private void jButton_ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ADDActionPerformed
        // TODO add your handling code here:
        try {
            // Mengambil nama dari form
            String nama = jTextField_Nama1.getText();
            if (nama.isEmpty() || nama.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong!", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Mendapatkan ID berikutnya yang tersedia (berurutan dari 1)
            int nextId = service.getNextId();
            
            // Membuat objek mahasiswa baru
            Mahasiswa mhs = new Mahasiswa(nextId, nama);
            service.add(mhs);
            idModel.clear();
            NamaModel.clear();
            List<Mahasiswa> listMhs = service.getAll();
            for (Mahasiswa mahasiswa : listMhs) {
                idModel.addElement(mahasiswa.getId());
                NamaModel.addElement(mahasiswa.getNama());
            }
            updateList();
        } catch (Exception e) {
            System.out.println("Error adding student: " + e.getMessage());
        }
        jTextField_Nama1.setText("");
    }//GEN-LAST:event_jButton_ADDActionPerformed

    private void jButton_HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_HapusActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(jTextField_ID2.getText());
            service.delete(id);
            jButton_AllDataActionPerformed(evt);
            List<Mahasiswa> listMhs = service.getAll();
            for (Mahasiswa mhs : listMhs) {
                idModel.addElement(mhs.getId());
                NamaModel.addElement(mhs.getNama());
            }
            updateList();
        } catch (NumberFormatException e) {
            System.out.println("Invalid ID format: " + e.getMessage());
        } catch (Exception e) {
            System.out.println("Error deleting student: " + e.getMessage());
        }
        jTextField_ID1.setText("");
        jTextField_Nama2.setText("");
        jTextField_ID2.setText("");
        jTextField_Nama3.setText("");
    }//GEN-LAST:event_jButton_HapusActionPerformed

